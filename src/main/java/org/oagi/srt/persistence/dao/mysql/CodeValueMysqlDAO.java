	public int insertObject(SRTObject obj) throws SRTDAOException {
		DBAgent tx = new DBAgent();
		Connection conn = null;
		PreparedStatement ps = null;
		ResultSet rs = null;

		CodeValueVO codevalueVO = (CodeValueVO) obj;
		try {
			conn = tx.open();
			ps = conn.prepareStatement(_INSERT_Code_Value_STATEMENT);
			ps.setInt(1, codevalueVO.getOwnerCodeListID());
			ps.setString(2, codevalueVO.getValue());
			ps.setString(3, codevalueVO.getDefinition());
			ps.setBoolean(4, codevalueVO.getUsedIndicator());
			ps.setBoolean(5, codevalueVO.getLockedIndicator());

			ps.executeUpdate();

			rs = ps.getGeneratedKeys();
			rs.next();
			//int autoGeneratedID = tableKeys.getInt(1);

			tx.commit();
		} catch (BfPersistenceException e) {
			tx.rollback();
			throw new SRTDAOException(SRTDAOException.DAO_INSERT_ERROR, e);
		} catch (SQLException e) {
			tx.rollback();
			throw new SRTDAOException(SRTDAOException.SQL_EXECUTION_FAILED, e);
		} finally {
			closeQuietly(rs);
			closeQuietly(ps);
			closeQuietly(conn);
			closeQuietly(tx);
		}
		return 1;
	}

	public SRTObject findObject(QueryCondition qc) throws SRTDAOException {
		DBAgent tx = new DBAgent();
		Connection conn = null;
		PreparedStatement ps = null;
		ResultSet rs = null;

		CodeValueVO codevalueVO = new CodeValueVO();
		try {
			conn = tx.open();
			String sql = _FIND_Code_Value_STATEMENT;

			String WHERE_OR_AND = " WHERE ";
			int nCond = qc.getSize();
			if (nCond > 0) {
				for (int n = 0; n < nCond; n++) {
					sql += WHERE_OR_AND + qc.getField(n) + " = ?";
					WHERE_OR_AND = " AND ";
				}
			}
			ps = conn.prepareStatement(sql);
			if (nCond > 0) {
				for (int n = 0; n < nCond; n++) {
					Object value = qc.getValue(n);
					if (value instanceof String) {
						ps.setString(n + 1, (String) value);
					} else if (value instanceof Integer) {
						ps.setInt(n + 1, ((Integer) value).intValue());
					}
				}
			}

			rs = ps.executeQuery();
			if (rs.next()) {
				codevalueVO.setCodeValueID(rs.getInt("Code_Value_ID"));
				codevalueVO.setOwnerCodeListID(rs.getInt("Owner_Code_List_ID"));
				codevalueVO.setValue(rs.getString("Value"));
				codevalueVO.setDefinition(rs.getString("Definition"));
				codevalueVO.setUsedIndicator(rs.getBoolean("Used_Indicator"));
				codevalueVO.setUsedIndicator(rs.getBoolean("Locked_Indicator"));
			}
			tx.commit();
		} catch (BfPersistenceException e) {
			throw new SRTDAOException(SRTDAOException.DAO_FIND_ERROR, e);
		} catch (SQLException e) {
			throw new SRTDAOException(SRTDAOException.SQL_EXECUTION_FAILED, e);
		} finally {
			closeQuietly(rs);
			closeQuietly(ps);
			closeQuietly(conn);
			closeQuietly(tx);
		}
		return codevalueVO;
	}

	public ArrayList<SRTObject> findObjects() throws SRTDAOException {
		DBAgent tx = new DBAgent();
		Connection conn = null;
		PreparedStatement ps = null;
		ResultSet rs = null;

		ArrayList<SRTObject> list = new ArrayList<SRTObject>();
		try {
			conn = tx.open();
			String sql = _FIND_ALL_Code_Value_STATEMENT;
			ps = conn.prepareStatement(sql);
			rs = ps.executeQuery();
			while (rs.next()) {
				CodeValueVO codevalueVO = new CodeValueVO();
				codevalueVO.setCodeValueID(rs.getInt("Code_Value_ID"));
				codevalueVO.setOwnerCodeListID(rs.getInt("Owner_Code_List_ID"));
				codevalueVO.setValue(rs.getString("Value"));
				codevalueVO.setDefinition(rs.getString("Definition"));
				codevalueVO.setUsedIndicator(rs.getBoolean("Used_Indicator"));
				codevalueVO.setUsedIndicator(rs.getBoolean("Locked_Indicator"));
				list.add(codevalueVO);
			}
			tx.commit();
		} catch (BfPersistenceException e) {
			throw new SRTDAOException(SRTDAOException.DAO_FIND_ERROR, e);
		} catch (SQLException e) {
			throw new SRTDAOException(SRTDAOException.SQL_EXECUTION_FAILED, e);
		} finally {
			closeQuietly(rs);
			closeQuietly(ps);
			closeQuietly(conn);
			closeQuietly(tx);
		}

		return list;
	}

