	private final String _tableName = "code_value";

	private final String _FIND_ALL_Code_Value_STATEMENT =
			"SELECT Code_Value_ID, Owner_Code_List_ID, Value, Definition, Used_Indicator, Locked_Indicator FROM " + _tableName;

	private final String _FIND_Code_Value_STATEMENT =
			"SELECT Code_Value_ID, Owner_Code_List_ID, Value, Definition, Used_Indicator, Locked_Indicator FROM " + _tableName;

	private final String _INSERT_Code_Value_STATEMENT =
			"INSERT INTO " + _tableName + " (Owner_Code_List_ID, Value, Definition, Used_Indicator, Locked_Indicator) VALUES (?, ?, ?, ?, ?)";

	private final String _UPDATE_Code_Value_STATEMENT =
			"UPDATE " + _tableName
			+ " SET Code_Value_ID = ?, Owner_Code_List_ID = ?, Value = ?, Definition = ?, Used_Indicator = ?, Locked_Indicator = ? WHERE Code_Value_ID = ?";

	private final String _DELETE_Code_Value_STATEMENT =
			"DELETE FROM " + _tableName + " WHERE Code_Value_ID = ?";

